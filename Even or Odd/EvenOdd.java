//Write a program to find Even or odd numbers
//90/2=45//quotient
//90%2=0//remainder
// if else condition
public class EvenOdd {
    public static void main(String[] args) {
        int no = 80;
        if (no % 2 == 0) {
            System.out.println("Number is even");
        } else {
            System.out.println("Number is odd");
        }
    }
}
